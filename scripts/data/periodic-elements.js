const PERIODIC_ELEMENT_WEIGHTS = PERIODIC_ELEMENTS.map((element) =>
  element && Number.isFinite(element.number) && element.number > 0
    ? 1 / element.number
    : 0
);

const PERIODIC_ELEMENT_TOTAL_WEIGHT = PERIODIC_ELEMENT_WEIGHTS.reduce(
  (sum, weight) => sum + weight,
  0
);

export const samplePeriodicElement = (randomFn = Math.random) => {
  if (
    PERIODIC_ELEMENTS.length === 0 ||
    !Number.isFinite(PERIODIC_ELEMENT_TOTAL_WEIGHT) ||
    PERIODIC_ELEMENT_TOTAL_WEIGHT <= 0
  ) {
    return null;
  }

  const randomValue = randomFn();
  const clampedRandom =
    Number.isFinite(randomValue) && randomValue >= 0 && randomValue <= 1
      ? randomValue
      : Math.random();
  const target = clampedRandom * PERIODIC_ELEMENT_TOTAL_WEIGHT;

  let cumulative = 0;
  for (let index = 0; index < PERIODIC_ELEMENTS.length; index += 1) {
    cumulative += PERIODIC_ELEMENT_WEIGHTS[index];
    if (target <= cumulative) {
      const element = PERIODIC_ELEMENTS[index];
      if (!element) {
        break;
      }
      // Return the full element object so downstream consumers receive all
      // available metadata instead of a pared-down subset.
      return { ...element };
    }
  }

  const fallback = PERIODIC_ELEMENTS[PERIODIC_ELEMENTS.length - 1];
  return fallback ? { ...fallback } : null;
};
